.INCLUDE "M644PDEF.INC"
.INCLUDE "fib_call.asm"

.ORG 0x0100

// INITIALIZATION
LDI R16, LOW(RAMEND) // STACK POINTER INITIALIZATION
OUT SPL, R16
MOV YL, R16 // SET OF REGISTER Y
LDI R16, HIGH(RAMEND)
OUT SPH, R16
MOV YH, R16

BEGIN:
LDI R16, 0X00	// N (COUNTER)
LDI R17, 0X00
LDI R18, 0X01
LDI R19, 0X00

LOOP2:
	PUSH R17 // 1ST. PARAMETR
	RCALL FIB
	POP R17 

	DEC R16
	BRNE LOOP2

RJMP BEGIN



