void Timer0_Init(void) {
//	OCR0A = 0x01;								// Output Compare Register A (stores the compare value)
	TCCR0A |= (1 << COM0A1);					// None-inverted mode (HIGH at bottom, LOW on Match)
	TCCR0A |= (1 << WGM00);						// PWM, Phase Correct, 8-bit (page 131, 148)
	TCCR0B |= (1 << CS01) | (1 << CS00);        // Bits 2:0 – CS02:0: Clock Select

}